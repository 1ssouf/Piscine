#include <stdlib.h>

int	*ft_range(int min, int max)
{
	int	*tab;
	int	i;	

	if (max <= min)
		return (0);
	tab = malloc(sizeof(*tab) * (max - min));
	if (!tab)
		return (0);
	i = 0;
	while (min < max)
	{
		tab[i] = min;
		i++;
		min++;
	}
	return (tab);
}
