int	ft_strcmp(char *s1, char *s2)
{
	int	a;

	a = 0;
	while (s1[a] != '\0' && s2[a] != '\0' && s1[a] == s2[a])
		a++;
	if (s1 == '\0' && s2 == '\0')
		return (0);
	else
		return (s1[a] - s2[a]);
}
